from random import choice
from utils import req
# -------------------------------------------------------------------------------------------------

# =================================================================================================
class Move():
    
    # ---------------------------------------------------------------------------------------------
    def __init__( self, inMoveID ):
        
        self.Move = req( "move", inMoveID )
        
        self.name = self.Move["name"]
        self.type = self.Move["type"]["name"]
        self.category = self.Move["damage_class"]["name"]
        self.power = self.Move["power"]
        self.accuracy = self.Move["accuracy"]
        self.priority = self.Move["priority"]
    # ---------------------------------------------------------------------------------------------
    
    
    # ---------------------------------------------------------------------------------------------
    def display_info( self ):
        print( f"Move    : {self.name}" )
        print( f"Type    : {self.type}" )
        print( f"Category: {self.category}" )
        print( f"Power   : {self.power}" )
        print( f"Accuracy: {self.accuracy}" )
        print( f"Priority: {self.priority}" )
    # ---------------------------------------------------------------------------------------------

